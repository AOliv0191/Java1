public class loops {
    public static void main(String[] args) {

    System.out.println("");


    for(int i = 0; i < 5; i++){
    
        System.out.println((i + 1) + ": Hello");

    }

    System.out.println("");
    
    int i = 0;

    while(i < 5) {

        System.out.println((i + 1) + ": Hello");
    i++;

    }

    System.out.println("");

    i = 0;

    do{
    
        System.out.println((i + 1) + ": Hello");
    i++;

    }
    while(i < 5);

    System.out.println("");

    }
}
